    private void btHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHapusActionPerformed
        // TODO add your handling code here:
        try {
            if ("".equals(txtKodeBuku.getText())) {
                JOptionPane.showMessageDialog(this, "Isikan Kode Buku terlebih dahulu");
            }else{
                String sql = "DELETE FROM data_buku WHERE kode_buku='" + txtKodeBuku.getText() + "'";
                java.sql.Connection conn = (Connection)Config.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus dengan Kode Buku " + txtKodeBuku.getText());
                writeLog("Data Berhasil Dihapus dengan Kode Buku " + txtKodeBuku.getText());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            writeLog("Data gagal dihapus : " + e.getMessage());
        }
        load_table();  
        bersihkan();
        
    }//GEN-LAST:event_btHapusActionPerformed

