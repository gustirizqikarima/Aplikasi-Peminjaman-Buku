    private void btTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTambahActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO data_buku VALUES ('"+txtKodeBuku.getText()+
                    "','"+txtJudulBuku.getText()+
                    "','"+txtPengarang.getText()+
                    "','"+txtPenerbit.getText()+
                    "','"+txtTahunTerbit.getText()+"')";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            writeLog("Penyimpanan Data Berhasil dengan NIM"+txtKodeBuku.getText());
            load_table();
            bersihkan();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            writeLog("Data gagal disimpan:"+e.getMessage());
        }                                       
    }//GEN-LAST:event_btTambahActionPerformed

    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        // TODO add your handling code here:
        try {
                if ("".equals(txtKodeBuku.getText())){
                    JOptionPane.showMessageDialog(this, "Isikan Kode Buku terlebih dahulu");
                }else{
                    String sql ="UPDATE data_buku SET judul_buku= '"+ txtJudulBuku.getText()+                    
                        "', pengarang ='"+txtPengarang.getText()+
                        "', Penerbit='"+txtPenerbit.getText()+
                        "', tahun_terbit ='"+txtTahunTerbit.getText()+
                        "' WHERE kode_buku = '"+txtKodeBuku.getText()+"'";
                    java.sql.Connection conn=(Connection)Config.configDB();
                    java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Diperbaharui dengan Kode Buku"+txtKodeBuku.getText());
                    writeLog("Data Berhasil Diperbaharui dengan ID "+txtKodeBuku.getText());
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Perubahan Data Gagal"+e.getMessage());
                writeLog("Perubahan Data Gagal : " + e.getMessage());
            }
            load_table();
            bersihkan();       
    }//GEN-LAST:event_btEditActionPerformed

